public class PositiveNegative  
{  
public static void main(String[] args)   
{  
int num=12;  
if(num>0)  
{  
System.out.println(num + "The number is positive.");  
}  
else if(num<0)  
{  
System.out.println(num + "The number is negative.");  
}  
else  
{  
System.out.println("The number is zero.");  
}  
}  
}